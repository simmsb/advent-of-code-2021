def line(s):
    rule = {
        ")": 3,
        "]": 57,
        "}": 1197,
        ">": 25137
    }

    p2rule = {
        "(": 1,
        "[": 2,
        "{": 3,
        "<": 4
    }

    inv = {
        ")": "(",
        "]": "[",
        "}": "{",
        ">": "<"
    }

    stack = []

    first = None

    for c in s:
        if c in "([{<":
            stack.append(c)
        elif c in ">}])":
            expect = stack.pop()
            if inv[c] != expect and first is None:
                first = rule[c]

    if first is not None:
        return (first, None)

    n = 0
    for c in stack[::-1]:
        n *= 5
        n += p2rule[c]

    return (0, n)


